      <p>Samantha   was born in the lovely land of enchantment on the eastern New Mexico   plains in a little village known as Melrose.  Growing up on the   family farm she learned how to work hard, believe in herself and most   importantly to be thankful for all the blessings in life.  When   Sam was sent out to quickly do the chores, her mother would find her   hours later, lost in imagination talking to the chickens or creating   a master piece of mud.  Although the creativity continued, in college   she choose to earn a B.S. in Family and Child Science at NMSU, where   she met her best friend and husband, Bruce Odom.  After a European   summer of art and a year long apprenticeship, Samantha began her career   in art.  Her work has been featured in The Show Case of Homes, <em> Ventanas</em>, local businesses and restaurants as well as residential   homes.   <br />
