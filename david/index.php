<p> Born February 2, 1986, David has been creating as long as he can remember. "The first thing I can remember drawing was a very crude tyrannosaurs rex. I think it started with 3/4 of a circle, eyes, and set of teeth."</p> 
<p>&nbsp;</p>
<p>David was accepted into The School of the Art Institute of Chicago's early college program (for high school students) in the summer of 2003 and enrolled for undergrad the following fall. In the summer of 2006 David attended the Art Academy in Prague, Czech Republic. During "Collateral" ( a show at the Avu gallery) David became the first student from the exchange programs 10 year history to sell a painting. David later finished his undergrad receiving a BFA in painting and drawing.</p> 
<p>&nbsp;</p>
<p>Realizing the importance of the computer in the contemporary art world David enrolled in New Mexico State's CMI department in pursuit of an associates of graphic design and will be awarded one in the fall of 2009. David currently resides in Kent, Ohio where he is working and preparing his portfolio for graduate work.</p></div> </div>
